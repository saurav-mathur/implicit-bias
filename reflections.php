        <p>
           Paired Programming: Although I have taken several computer sciences courses here at Madison which involved a significant amount of group work, this was the first time I participated in a paired programming session while not being in the same room as my partner. The overall idea remained the same – one of us would actually type in the code while the other would guide them and review the code typed, switching between the roles regularly. We used Microsoft Teams to achieve this because it has a great screen sharing feature which even has the ability to control someone else’s computer via the Teams call. Overall, I would say the experience was comparable to that of doing so in person, even having the added benefit of the two of us not having to compete for screen real estate allowing for a better workflow.
           </p>
           <p>
Project Work: Since this course was the first time I worked with PHP and SQL, it was a bit daunting at first – I initially had no idea of how to even start the project. Then I decided to work through the assignments for the course itself and realized that the assignments had slowly been building up my knowledge of different aspects of web development – Assignment 2 taught me how to build a website with HTML and CSS, and the next two taught me about PHP and SQL. Our team then decided to use the Assignment 4 as a starting point to make a skeleton survey. After the skeleton was ready, we divided up the work – one team work on the content and the other team would make the website – I mainly worked on developing the website, adding and/or creating pages as needed, making iterative changes to see what worked and what didn’t and ending up with a truly unique aesthetic to our site. I also created helpec creat ethe content on the homepage, the results page, and the resources page.</p>
<p>
Once we had submitted the first draft, we received feedback from our peers as well as from the instructors. The feedback was mostly positive with two specific points we were told we could improve on:<br>
1.	A question which talked about preferring a medical professional or lawyer of the same race.<br>
2.	We did not have enough content that explained what our test was about<br>
<p>We decided to keep the question in point 1 as we felt that even though the questions might be drastically different from the perspective of a POC vs a non-POC, it still allowed one to gain an insight on their view of race. As for the second point, we added more content to the homepage which explains what our test is about and added media that would allow the test taker to learn more about the reasoning behind the test.</p>
<p>
LIS 500: As for the course, I felt that I learnt a lot, (and have many more questions) than I did before taking this course. At the start of this course, I expected to lean a bit of web development and a little about how that directly affects people. In reality, I learn a lot more about PHP and SQL as well the presumptions and pitfalls that plaque today’s tech industry and the world beyond. The book Algorithms of Oppression was especially an important aspect of my learning this semester – applying many of the concepts we learnt in this course. The lectures and reading were organized and the quizzes helped me check my progress every week – allowing me to see if I needed to relearn or look at a concept I might have missed. Overall, I found this course interesting, informative, and really well managed (even after being totally online!)

        </p>
